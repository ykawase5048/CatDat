<h2>How to contribute</h2>

<p>
	<i>CatDat</i> is developed within an open-source
	<a href="https://github.com/ScriptRaccoon/catdat" target="_blank">GitHub repository</a
	>
	owned by
	<a href="https://ncatlab.org/nlab/show/Martin+Brandenburg" target="_blank"
		>Martin Brandenburg</a
	>. It welcomes contributions from the community to fill in missing information, or
	observe new combinations of properties.
</p>

<p>There are two canonical options to contribute to this project:</p>

<ul>
	<li>
		Either create a
		<a href="https://github.com/ScriptRaccoon/CatDat/pulls" target="_blank"
			>pull request</a
		>,
	</li>
	<li>
		or create an
		<a href="https://github.com/ScriptRaccoon/CatDat/issues" target="_blank">issue</a>
		on GitHub.
	</li>
</ul>

<p>
	In any case, please follow the
	<a
		href="https://github.com/ScriptRaccoon/CatDat/blob/main/CONTRIBUTING.md"
		target="_blank">contribution guidelines</a
	>!
</p>

<p>
	A third option is offered via a <a
		href="https://forms.gle/8LBmro5FfSa43pD2A"
		target="_blank">Google form</a
	>. This option does not require any knowledge of GitHub or coding.
</p>
